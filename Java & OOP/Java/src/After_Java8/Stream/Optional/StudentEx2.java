package Java.Stream.Optional;

public class StudentEx2 {
    private String name;
    private String sex;
    private int score;

    public StudentEx2(String name, String sex, int score) {
        this.name = name;
        this.sex = sex;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public String getSex() {
        return sex;
    }

    public int getScore() {
        return score;
    }
}
